package ch.hslu.oop.sw04schnittstellen_datenkapselung.point;

public class datenk_Point_point {
    private int x, y;

    public datenk_Point_point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}